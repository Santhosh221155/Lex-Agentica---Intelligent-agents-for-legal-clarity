import logging
import json
import time
import uuid
from contextvars import ContextVar

# Prometheus is optional for local smoke tests
try:
    from prometheus_client import Counter, Histogram, Gauge
    _HAS_PROM = True
except Exception:
    _HAS_PROM = False

logger = logging.getLogger("agentic_rag")

# Metrics (no-op fallbacks when prometheus_client isn't installed)
if _HAS_PROM:
    REQUEST_COUNTER = Counter("agentic_requests_total", "Total queries received")
    REQUEST_DURATION = Histogram("agentic_request_duration_seconds", "HTTP request duration seconds", ["method", "path", "status"])
    RETRIEVAL_LATENCY = Histogram("retrieval_latency_seconds", "Retrieval latency seconds")
    LLM_LATENCY = Histogram("llm_latency_seconds", "LLM call latency seconds")
    RERANKER_LATENCY = Histogram("reranker_latency_seconds", "Reranker latency seconds")
    AGENT_LATENCY = Histogram("agent_latency_seconds", "Agent latency seconds", ["agent"])
    CACHE_HITS = Counter("cache_hits_total", "Cache hit count")
    CACHE_MISSES = Counter("cache_misses_total", "Cache miss count")
    TOKEN_USAGE = Counter("llm_tokens_generated_total", "Approximate tokens generated by LLMs")
    TOOL_USAGE = Counter("tool_usage_total", "Tool executions", ["tool", "status"])
    VALIDATION_FAILURES = Counter("validation_failures_total", "Validation failures", ["kind"])
    HALLUCINATION_DETECTIONS = Counter("hallucination_detections_total", "Hallucination detections", ["severity"])
    COST_ESTIMATE = Counter("llm_cost_estimate_usd_total", "Estimated LLM cost in USD")
    ERROR_COUNTER = Counter("agentic_errors_total", "Total handled errors", ["component", "kind"])
else:
    class _Noop:
        def observe(self, *a, **k):
            pass

        def inc(self, *a, **k):
            pass

    REQUEST_COUNTER = _Noop()
    REQUEST_DURATION = _Noop()
    RETRIEVAL_LATENCY = _Noop()
    LLM_LATENCY = _Noop()
    RERANKER_LATENCY = _Noop()
    AGENT_LATENCY = _Noop()
    CACHE_HITS = _Noop()
    CACHE_MISSES = _Noop()
    TOKEN_USAGE = _Noop()
    TOOL_USAGE = _Noop()
    VALIDATION_FAILURES = _Noop()
    HALLUCINATION_DETECTIONS = _Noop()
    COST_ESTIMATE = _Noop()
    ERROR_COUNTER = _Noop()


