def inc_request():
    try:
        REQUEST_COUNTER.inc()
    except Exception:
        pass


def observe_request(method: str, path: str, status: int, duration: float):
    try:
        REQUEST_DURATION.labels(method=method, path=path, status=str(status)).observe(duration)
    except Exception:
        pass


def inc_error(component: str, kind: str = "error"):
    try:
        ERROR_COUNTER.labels(component=component, kind=kind).inc()
    except Exception:
        pass


def new_trace_id() -> str:
    return uuid.uuid4().hex


def set_trace_id(trace_id: str):
    TRACE_ID.set(trace_id)


def get_trace_id() -> str:
    return TRACE_ID.get()


def log_event(event: str, **fields):
    if event.startswith("retrieval.") and event not in _ALLOWED_RETRIEVAL_EVENTS:
        return
    payload = {
        "event": event,
        "trace_id": fields.pop("trace_id", get_trace_id()),
        "timestamp": time.time(),
        **fields,
    }
    try:
        logger.info(json.dumps(payload, default=str))
    except Exception:
        logger.info("%s %s", event, payload)


