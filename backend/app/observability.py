_OTEL_INITIALIZED = False


def init_opentelemetry(service_name: str = "agentic_rag"):
    """Best-effort initialize OpenTelemetry tracing and instrumentations.

    This function lazy-imports opentelemetry packages and instruments FastAPI,
    requests, and SQLAlchemy when available. If the required packages are not
    installed, this is a no-op.
    """
    global _OTEL_INITIALIZED
    if _OTEL_INITIALIZED:
        return True
    try:
        from opentelemetry import trace
        from opentelemetry.sdk.resources import Resource
        from opentelemetry.sdk.trace import TracerProvider
        from opentelemetry.sdk.trace.export import BatchSpanProcessor
        try:
            from opentelemetry.exporter.otlp.proto.http.trace_exporter import OTLPSpanExporter
        except Exception:
            try:
                from opentelemetry.exporter.otlp.trace_exporter import OTLPSpanExporter
            except Exception:
                OTLPSpanExporter = None

        if OTLPSpanExporter is None:
            return False

        endpoint = None
        # standard env vars: OTEL_EXPORTER_OTLP_ENDPOINT or OTEL_EXPORTER_OTLP_TRACES_ENDPOINT
        import os

        endpoint = os.getenv("OTEL_EXPORTER_OTLP_TRACES_ENDPOINT") or os.getenv("OTEL_EXPORTER_OTLP_ENDPOINT")

        resource = Resource.create({"service.name": service_name})
        provider = TracerProvider(resource=resource)
        trace.set_tracer_provider(provider)

        exporter_kwargs = {}
        if endpoint:
            exporter_kwargs["endpoint"] = endpoint
        exporter = OTLPSpanExporter(**exporter_kwargs) if OTLPSpanExporter is not None else None
        if exporter is not None:
            span_processor = BatchSpanProcessor(exporter)
            provider.add_span_processor(span_processor)

        # instrument frameworks where possible
        try:
            from opentelemetry.instrumentation.fastapi import FastAPIInstrumentor
            # Application will instrument FastAPI at startup (main.py) by calling this module
            FastAPIInstrumentor.instrument_app = FastAPIInstrumentor.instrument_app  # noqa: E702
        except Exception:
            pass

        try:
            from opentelemetry.instrumentation.requests import RequestsInstrumentor
            RequestsInstrumentor().instrument()
        except Exception:
            pass

        try:
            from opentelemetry.instrumentation.sqlalchemy import SQLAlchemyInstrumentor
            SQLAlchemyInstrumentor().instrument()
        except Exception:
            pass

        _OTEL_INITIALIZED = True
        logger.info("OpenTelemetry initialized (endpoint=%s)", endpoint)
        return True
    except Exception as e:
        logger.debug("OpenTelemetry not initialized: %s", e)
        return False
